--
-- Tabella delle sessioni
--
CREATE TABLE IF NOT EXISTS sols (
  id INTEGER NOT NULL PRIMARY KEY,
  pol_id INTEGER NOT NULL REFERENCES pols(id) ON DELETE CASCADE ON UPDATE CASCADE,
  name VARCHAR( 255 ) NOT NULL,
  start_time TIMESTAMP NOT NULL DEFAULT '1990-01-01 00:00:00',
  end_time TIMESTAMP NOT NULL DEFAULT '1990-01-01 00:00:00',
  decoding_start TIMESTAMP NOT NULL DEFAULT '1990-01-01 00:00:00',
  decoding_end TIMESTAMP NOT NULL DEFAULT '1990-01-01 00:00:00',
  status VARCHAR( 40 ) DEFAULT 'EMPTY',
  rm INTEGER DEFAULT 0
);
